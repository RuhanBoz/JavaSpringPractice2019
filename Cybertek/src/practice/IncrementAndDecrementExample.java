package practice;

public class IncrementAndDecrementExample {

	public static void main(String[] args) {

		int a,b,c;
		
		a=5;
		
		b=a++; // burada b=5 olur ve a 6 olur
		
		c=a++;
		
		
		System.out.println("b nin degeri "+b);
		
		System.out.println("c nin degeri "+c);
		
		System.out.println("a nin final degeri "+a);
	
	
	int d=10;
	
	int f=++d; // f=11 olur and d=11 olur
	
	
	
	System.out.println(d);
	
	System.out.println(f++);
	
	
	System.out.println(f);
	
	
	int x=8;
	
	int y;
	
	y=--x;
	
	++y;
	
	System.out.println(x);
	
	System.out.println(y);
	
	
	
	}
}
