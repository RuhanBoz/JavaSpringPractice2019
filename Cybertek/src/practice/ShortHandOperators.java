package practice;

public class ShortHandOperators {

	public static void main(String[] args) {

		
		int a=5;
		
		a+=2; // bu a=a+2 ile ayni sey demektir. Kullanisli olan yol bu
		
		System.out.println(a);
		
		int b=8;
		
		System.out.println("b nin onceki degeri "+b);
		b+=3;
		
		
		System.out.println("b nin sonraki degeri "+b);
		
		int c=10;
		int d=15;
		c*=2;
		
		System.out.println(c);
		
		d/=4;
		
		System.out.println(d);

		int t=23;
		
		t%=5;
		
		System.out.println(t);
		
		int x=12;
		int  y=15;
		
		x--;
		++y;
		
		y+=x;
		
		System.out.println(x);
		
		System.out.println(y);
		
		
		int w=4;
		int s=10;
		
		s+=w; // s=s+w; s=10+4;
		
		
		
		
		int num1=6;
		int num2=10;
		
		num1=num2++;
		
		
		num1*=num2;
		
		num2-=2;
		
		
		System.out.println(num1);
		
		System.out.println(num2);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
