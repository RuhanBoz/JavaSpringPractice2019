package practice;

public class SwapAssignment {

	public static void main(String[] args) {

		
	   int a = 10;         //a=15;
	   int b = 15;         //b=10;
	   
	     System.out.println(a);
	     System.out.println(b);
	   
	   a = a+b;            //25;
	   b = a-b;            //10
	   a = a-b;            //15
	   
	   
	   System.out.println(a);
	   System.out.println(b);
		
		}

}
